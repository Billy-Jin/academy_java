package ch_variable;

public class FirstMyClass {

	public static void main(String[] args) {
		// 자바 한줄 주석문 --오라클 한줄 주석문
		/* 자바
		 * 여러줄
		 * 주석문
		 */
		System.out.println("출력");
		System.out.println("2번째 줄");
		
		
		//escape문자 
		System.out.print("3번째 줄\n");
		System.out.print("4번째 줄");
		System.out.print("\t\t\t\t5번째 줄\n");		
		System.out.print("\"큰따옴표\n");
		System.out.print("\'작은 따옴표\n");
		System.out.print('a'); //작은 따옴표 안에는 공백 포함 한글자만 출력가능
		
	}

}